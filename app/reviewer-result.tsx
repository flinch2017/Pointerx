function escapeHtml(value: string) {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

function reviewerToHtml(fileName: string, reviewer: string) {
  const body = escapeHtml(reviewer)
    .split('\n')
    .map((line) => {
      if (line.startsWith('# ')) {
        return `<h1>${line.slice(2)}</h1>`;
      }

      if (line.startsWith('## ')) {
        return `<h2>${line.slice(3)}</h2>`;
      }

      if (!line.trim()) {
        return '<br />';
      }

      return `<p>${line}</p>`;
    })
    .join('');

  return `<!doctype html>
<html>
  <head>
    <meta charset="utf-8" />
    <style>
      body {
        color: #3E3028;
        font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", sans-serif;
        padding: 32px;
      }
      h1 {
        color: #9C6B4E;
        font-size: 28px;
        margin: 0 0 18px;
      }
      h2 {
        border-bottom: 1px solid #E4CDB7;
        font-size: 19px;
        margin: 24px 0 10px;
        padding-bottom: 6px;
      }
      p {
        font-size: 13px;
        line-height: 1.55;
        margin: 0 0 8px;
      }
      .source {
        color: #7E6B5D;
        font-size: 12px;
        margin-bottom: 22px;
      }
    </style>
  </head>
  <body>
    <div class="source">Generated by Pointerx from ${escapeHtml(fileName)}</div>
    ${body}
  </body>
</html>`;
}

export default function ReviewerResultScreen() {
  const { palette } = useAppTheme();
  const styles = createStyles(palette);
  const reviewer = getLatestReviewer();
  const [status, setStatus] = useState<string | null>(null);
  const [error, setError] = useState<string | null>(null);

  async function saveReviewerAsPdf() {
    if (!reviewer) {
      return;
    }

    setError(null);
    setStatus('Creating PDF file...');

    try {
      const { uri } = await Print.printToFileAsync({
        html: reviewerToHtml(reviewer.fileName, reviewer.reviewer),
      });

      if (await Sharing.isAvailableAsync()) {
        await Sharing.shareAsync(uri, {
          dialogTitle: 'Save Pointerx reviewer',
          mimeType: 'application/pdf',
          UTI: 'com.adobe.pdf',
        });
        setStatus('PDF ready to save.');
      } else {
        setStatus(`PDF created at ${uri}`);
      }
    } catch (saveError) {
      const message =
        saveError instanceof Error ? saveError.message : 'Could not save reviewer as PDF.';
      setError(message);
      setStatus(null);
    }
  }

  if (!reviewer) {
    return (
      <View style={[styles.screen, styles.emptyState]}>
        <CircleBackButton fallback="/reviewer-converter" style={styles.emptyBackButton} />
        <Text style={styles.title}>No reviewer yet.</Text>
        <Text style={styles.subtitle}>Convert a PDF first to see the generated reviewer here.</Text>
        <Pressable onPress={() => router.replace('/reviewer-converter')} style={styles.primaryButton}>
          <Text style={styles.primaryButtonText}>Select PDF</Text>
        </Pressable>
      </View>
    );
  }

  return (
    <ScrollView
      contentInsetAdjustmentBehavior="automatic"
      style={styles.screen}
      contentContainerStyle={styles.content}>
      <CircleBackButton fallback="/reviewer-converter" />

      <View style={styles.header}>
        <Text style={styles.kicker}>Reviewer Result</Text>
        <Text style={styles.title}>{reviewer.fileName}</Text>
        <Text selectable style={styles.subtitle}>
          {reviewer.pageCount ?? 'Unknown'} pages ·{' '}
          {reviewer.processedCharacters.toLocaleString()} characters processed
        </Text>
      </View>

      <Pressable onPress={saveReviewerAsPdf} style={styles.primaryButton}>
        <IconSymbol name="doc.text.fill" color={palette.white} size={19} />
        <Text style={styles.primaryButtonText}>Save as PDF</Text>
      </Pressable>

      {status ? <Text style={styles.statusText}>{status}</Text> : null}
      {error ? <Text style={styles.errorText}>{error}</Text> : null}

      <View style={styles.resultPanel}>
        <Text selectable style={styles.reviewerText}>
          {reviewer.reviewer}
        </Text>
      </View>
    </ScrollView>
  );
}

